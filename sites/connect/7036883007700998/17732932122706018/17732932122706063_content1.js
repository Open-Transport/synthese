function client_add_click()
{
  $('#client_add_actionParam_table_id_AUTOGENERATEDFIELDID').val($(this).attr('data-table'));
  $('#client_add_type_span').html($(this).attr('data-type'));
  $('#client_add_modal').modal('show');
  return false;
}

function client_remove_click()
{
  $('#client_remove_actionParam_object_id_AUTOGENERATEDFIELDID').val($(this).attr('data-id'));
  $('#client_remove_name_span').html($(this).parent().find('span[data-content=name]').html());
  $('#client_remove_modal').modal('show');
  return false;
}

$(function(){
  $('#inter-synthese_client_add_link').click(client_add_click);
  $('#inter-synthese_client_add_link').tooltip();
  $('#vdv_client_add_link').click(client_add_click);
  $('#vdv_client_add_link').tooltip();
  $('#client_remove_link').click(client_remove_click);
  $('#client_remove_link').tooltip();
});