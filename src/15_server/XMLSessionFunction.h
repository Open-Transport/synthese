#ifndef SYNTHESE_XMLSessionFunction_H__
#define SYNTHESE_XMLSessionFunction_H__

#include "FactorableTemplate.h"
#include "Function.h"

namespace synthese
{
	namespace cms
	{
		class Webpage;
	}

	namespace server
	{
		//////////////////////////////////////////////////////////////////////////
		/// Returns informations about the current user session.
		/// See https://extranet.rcsmobility.com/projects/synthese/wiki/Session_display
		//////////////////////////////////////////////////////////////////////////
		///	@author Hugues Romain
		///	@date 2009
		///	@ingroup m15Functions refFunctions
		class XMLSessionFunction:
			public util::FactorableTemplate<server::Function,XMLSessionFunction>
		{
		public:
			static const std::string PARAMETER_CMS_TEMPLATE_ID;

		private:
			static const std::string ATTR_SESSION_ID;
			boost::shared_ptr<const cms::Webpage> _cmsTemplate;

		protected:


			//////////////////////////////////////////////////////////////////////////
			/// Conversion from attributes to generic parameter maps.
			/// See https://extranet.rcsmobility.com/projects/synthese/wiki/Session_display#Request
			//////////////////////////////////////////////////////////////////////////
			///	@return Generated parameters map
			/// @author Hugues Romain
			/// @date 2009
			util::ParametersMap _getParametersMap() const;



			//////////////////////////////////////////////////////////////////////////
			/// Conversion from generic parameters map to attributes.
			/// See https://extranet.rcsmobility.com/projects/synthese/wiki/Session_display#Request
			//////////////////////////////////////////////////////////////////////////
			///	@param map Parameters map to interpret
			/// @author Hugues Romain
			/// @date 2009
			virtual void _setFromParametersMap(
				const util::ParametersMap& map
			);


		public:
			//////////////////////////////////////////////////////////////////////////
			/// Display of the content generated by the function.
			/// See https://extranet.rcsmobility.com/projects/synthese/wiki/Session_display#XML-Response
			//////////////////////////////////////////////////////////////////////////
			/// @param stream Stream to display the content on.
			/// @author Hugues Romain
			/// @date 2009
			virtual util::ParametersMap run(
				std::ostream& stream,
				const server::Request& request
			) const;



			//////////////////////////////////////////////////////////////////////////
			/// Gets if the function can be run according to the user of the session.
			/// @return true if the function can be run
			/// @author Hugues Romain
			/// @date 2009
			virtual bool isAuthorized(const server::Session* session) const;



			//////////////////////////////////////////////////////////////////////////
			/// Gets the Mime type of the content generated by the function.
			/// @return the Mime type of the content generated by the function
			/// @author Hugues Romain
			/// @date 2009
			virtual std::string getOutputMimeType() const;
		};
}	}

#endif // SYNTHESE_XMLSessionFunction_H__
